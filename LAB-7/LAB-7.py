def evaluate_cnn(model, X_data, y_data):
    correct = 0
    total_loss = 0
    for i in range(len(X_data)):
        y_hat = model.forward(X_data[i])

        loss = - (y_data[i]*np.log(y_hat+1e-8) +
                 (1-y_data[i])*np.log(1-y_hat+1e-8))
        total_loss += loss
        if (y_hat >= 0.5) == y_data[i]:
            correct += 1
    return correct/len(X_data), total_loss/len(X_data)

class SGD_CNN:
    def __init__(self, lr):
        self.lr = lr
    def step(self, model):
        model.conv.K -= self.lr * model.conv.dK
        model.conv.b -= self.lr * model.conv.db
        model.fc.W -= self.lr * model.fc.dW
        model.fc.b -= self.lr * model.fc.db

class Momentum_CNN:
    def __init__(self, lr, beta=0.9):
        self.lr = lr
        self.beta = beta
        self.v = {}
    def step(self, model):
        if not self.v:
            self.v["convK"] = np.zeros_like(model.conv.K)
            self.v["convb"] = 0
            self.v["fcW"] = np.zeros_like(model.fc.W)
            self.v["fcb"] = np.zeros_like(model.fc.b)
        self.v["convK"] = self.beta*self.v["convK"] + self.lr*model.conv.dK
        self.v["convb"] = self.beta*self.v["convb"] + self.lr*model.conv.db
        self.v["fcW"] = self.beta*self.v["fcW"] + self.lr*model.fc.dW
        self.v["fcb"] = self.beta*self.v["fcb"] + self.lr*model.fc.db
        model.conv.K -= self.v["convK"]
        model.conv.b -= self.v["convb"]
        model.fc.W -= self.v["fcW"]
        model.fc.b -= self.v["fcb"]

class Adam_CNN:
    def __init__(self, lr, beta1=0.9, beta2=0.999, eps=1e-8):
        self.lr = lr
        self.beta1 = beta1
        self.beta2 = beta2
        self.eps = eps
        self.m = {}
        self.v = {}
    def step(self, model):
        if not self.m:
            self.m["convK"] = np.zeros_like(model.conv.K)
            self.m["convb"] = 0
            self.m["fcW"] = np.zeros_like(model.fc.W)
            self.m["fcb"] = np.zeros_like(model.fc.b)
            self.v["convK"] = np.zeros_like(model.conv.K)
            self.v["convb"] = 0
            self.v["fcW"] = np.zeros_like(model.fc.W)
            self.v["fcb"] = np.zeros_like(model.fc.b)
        grads = {
            "convK": model.conv.dK,
            "convb": model.conv.db,
            "fcW": model.fc.dW,
            "fcb": model.fc.db
        }
        for key in grads:
            g = grads[key]
            self.m[key] = self.beta1*self.m[key] + (1-self.beta1)*g
            self.v[key] = self.beta2*self.v[key] + (1-self.beta2)*(g**2)
            update = self.lr * self.m[key] / (np.sqrt(self.v[key]) + self.eps)
            if key == "convK":
                model.conv.K -= update
            elif key == "convb":
                model.conv.b -= update
            elif key == "fcW":
                model.fc.W -= update
            elif key == "fcb":
                model.fc.b -= update

def train_with_optimizer(optimizer_class, lr=0.01, epochs=5):

    model = SimpleCNN()
    optimizer = optimizer_class(lr)

    train_loss_hist = []
    val_loss_hist   = []
    train_acc_hist  = []
    val_acc_hist    = []

    for epoch in range(epochs):

        total_loss = 0
        correct = 0

        for i in range(len(X_train)):
            X_sample = X_train[i]
            y_sample = y_train[i].reshape(1,1)

            y_hat = model.forward(X_sample)

            loss = - (y_sample*np.log(y_hat+1e-8) +
                     (1-y_sample)*np.log(1-y_hat+1e-8))

            total_loss += loss.item()

            if (y_hat >= 0.5) == y_sample:
                correct += 1

            model.backward(y_sample)
            optimizer.step(model)

        train_loss = total_loss / len(X_train)
        train_acc  = correct / len(X_train)

        val_correct = 0
        val_loss_total = 0

        for i in range(len(X_val)):
            y_hat = model.forward(X_val[i])

            loss = - (y_val[i]*np.log(y_hat+1e-8) +
                     (1-y_val[i])*np.log(1-y_hat+1e-8))

            val_loss_total += loss.item()

            if (y_hat >= 0.5) == y_val[i]:
                val_correct += 1

        val_loss = val_loss_total / len(X_val)
        val_acc  = val_correct / len(X_val)

        train_loss_hist.append(train_loss)
        val_loss_hist.append(val_loss)
        train_acc_hist.append(train_acc)
        val_acc_hist.append(val_acc)

    plt.figure()
