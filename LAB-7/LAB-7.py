class SGD:
    def __init__(self, lr):
        self.lr = lr
    def step(self, model):
        for l in range(1, model.L+1):
            model.W[l] -= self.lr * model.dW[l]
            model.b[l] -= self.lr * model.db[l]

class Momentum:
    def __init__(self, lr, beta=0.9):
        self.lr = lr
        self.beta = beta
        self.vW = {}
        self.vb = {}
    def step(self, model):
        for l in range(1, model.L+1):
            if l not in self.vW:
                self.vW[l] = np.zeros_like(model.W[l])
                self.vb[l] = np.zeros_like(model.b[l])
            self.vW[l] = self.beta*self.vW[l] + self.lr*model.dW[l]
            self.vb[l] = self.beta*self.vb[l] + self.lr*model.db[l]
            model.W[l] -= self.vW[l]
            model.b[l] -= self.vb[l]

