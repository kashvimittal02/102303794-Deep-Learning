class Dropout:
    def __init__(self, p=0.5):
        self.p = p
    def forward(self, X, training=True):
        if training:
            self.mask = (np.random.rand(*X.shape) > self.p).astype(float)
            return X * self.mask
        else:
            return X * (1 - self.p)
    def backward(self, dA):
        return dA * self.mask

