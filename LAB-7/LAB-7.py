class CNN_WithDropout:
    def __init__(self, p=0.5):
        self.conv = ConvLayer(3)
        self.relu = ReLU()
        self.dropout = Dropout(p)
        self.pool = MaxPool(2)
        self.flatten = Flatten()
        self.fc = DenseOutput(9)
    def forward(self, X, training=True):
        z = self.conv.forward(X)
        a = self.relu.forward(z)
        d = self.dropout.forward(a, training)
        p = self.pool.forward(d)
        f = self.flatten.forward(p)
        out = self.fc.forward(f)
        return out
    def backward(self, y):
        d_fc = self.fc.backward(y)
        d_flat = self.flatten.backward(d_fc)
        d_pool = self.pool.backward(d_flat)
        d_drop = self.dropout.backward(d_pool)
        d_relu = self.relu.backward(d_drop)
        self.conv.backward(d_relu)

