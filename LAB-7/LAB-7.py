class ConvLayer:
    def __init__(self, filter_size=3):
        self.F = filter_size
        self.K = np.random.randn(self.F, self.F) * 0.01
        self.b = 0.0
    def forward(self, X):
        self.X = X
        H, W = X.shape
        out_dim = H - self.F + 1
        self.Z = np.zeros((out_dim, out_dim))
        for i in range(out_dim):
            for j in range(out_dim):
                region = X[i:i+self.F, j:j+self.F]
                self.Z[i,j] = np.sum(region * self.K) + self.b
        return self.Z

    def backward(self, dZ):
        H, W = self.X.shape
        out_dim = H - self.F + 1
        self.dK = np.zeros_like(self.K)
        self.db = 0.0
        dX = np.zeros_like(self.X)
        for i in range(out_dim):
            for j in range(out_dim):
                region = self.X[i:i+self.F, j:j+self.F]
                self.dK += region * dZ[i,j]
                dX[i:i+self.F, j:j+self.F] += self.K * dZ[i,j]
                self.db += dZ[i,j]
        return dX

class ReLU:
    def forward(self, X):
        self.X = X
        return np.maximum(0, X)
    def backward(self, dA):
        return dA * (self.X > 0)

class MaxPool:
    def __init__(self, size=2):
        self.size = size
    def forward(self, X):
        self.X = X
        H, W = X.shape
        out_dim = H // self.size
        self.Z = np.zeros((out_dim, out_dim))
        self.max_mask = np.zeros_like(X)
        for i in range(out_dim):
            for j in range(out_dim):
                region = X[i*self.size:(i+1)*self.size,
                           j*self.size:(j+1)*self.size]
                max_val = np.max(region)
                self.Z[i,j] = max_val
                for m in range(self.size):
                    for n in range(self.size):
                        if region[m,n] == max_val:
                            self.max_mask[i*self.size+m,
                                          j*self.size+n] = 1
        return self.Z

    def backward(self, dZ):
        H, W = self.X.shape
        dX = np.zeros_like(self.X)
        out_dim = dZ.shape[0]
        for i in range(out_dim):
            for j in range(out_dim):
                for m in range(self.size):
                    for n in range(self.size):
                        if self.max_mask[i*self.size+m,
                                         j*self.size+n] == 1:
                            dX[i*self.size+m,
                               j*self.size+n] = dZ[i,j]
        return dX

class Flatten:
    def forward(self, X):
        self.shape = X.shape
        return X.reshape(1,-1)
    def backward(self, dA):
        return dA.reshape(self.shape)

class DenseOutput:
    def __init__(self, input_size):
        self.W = np.random.randn(input_size,1) * 0.01
        self.b = np.zeros((1,1))
    def forward(self, X):
        self.X = X
        self.Z = X @ self.W + self.b
        self.A = 1/(1+np.exp(-self.Z))
        return self.A
    def backward(self, y):
        m = y.shape[0]
        dZ = self.A - y
        self.dW = self.X.T @ dZ
        self.db = dZ
        dX = dZ @ self.W.T
        return dX

class SimpleCNN:
    def __init__(self):
        self.conv = ConvLayer(3)
        self.relu = ReLU()
        self.pool = MaxPool(2)
        self.flatten = Flatten()
        self.fc = DenseOutput(9)
    def forward(self, X):
        z = self.conv.forward(X)
        a = self.relu.forward(z)
        p = self.pool.forward(a)
        f = self.flatten.forward(p)
        out = self.fc.forward(f)
        return out
    def backward(self, y):
        d_fc = self.fc.backward(y)
        d_flat = self.flatten.backward(d_fc)
        d_pool = self.pool.backward(d_flat)
        d_relu = self.relu.backward(d_pool)
        d_conv = self.conv.backward(d_relu)

def train_cnn(model, X_train, y_train, epochs=5, lr=0.01):
    for epoch in range(epochs):
        total_loss = 0
        for i in range(len(X_train)):
            X_sample = X_train[i]
            y_sample = y_train[i].reshape(1,1)
            y_hat = model.forward(X_sample)
            loss = - (y_sample*np.log(y_hat+1e-8) +
                     (1-y_sample)*np.log(1-y_hat+1e-8))
            total_loss += loss
            model.backward(y_sample)
            model.conv.K -= lr * model.conv.dK
            model.conv.b -= lr * model.conv.db
            model.fc.W -= lr * model.fc.dW
            model.fc.b -= lr * model.fc.db
        print("Epoch:", epoch, "Loss:", total_loss/len(X_train))

