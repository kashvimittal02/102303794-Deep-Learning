def sigmoid(z):
    return 1 / (1 + np.exp(-z))
def dsigmoid(a):
    return a * (1 - a)
def relu(z):
    return np.maximum(0, z)
def drelu(z):
    return (z > 0).astype(float)

def bce_loss(y, y_hat):
    eps = 1e-8
    y_hat = np.clip(y_hat, eps, 1-eps)
    return -np.mean(y*np.log(y_hat) + (1-y)*np.log(1-y_hat))
def accuracy(y, y_hat):
    return np.mean((y_hat >= 0.5) == y)

class DenseNetwork:
    def __init__(self, layer_sizes, activation='sigmoid'):
        self.L = len(layer_sizes) - 1
        self.activation_type = activation
        self.W = {}
        self.b = {}
        for l in range(1, self.L+1):
            self.W[l] = np.random.randn(layer_sizes[l-1], layer_sizes[l]) * 0.1
            self.b[l] = np.zeros((1, layer_sizes[l]))

    def forward(self, X):
        self.A = {0: X}
        self.Z = {}
        for l in range(1, self.L+1):
            self.Z[l] = self.A[l-1] @ self.W[l] + self.b[l]
            if l == self.L:
                self.A[l] = sigmoid(self.Z[l])
            else:
                if self.activation_type == 'sigmoid':
                    self.A[l] = sigmoid(self.Z[l])
                else:
                    self.A[l] = relu(self.Z[l])
        return self.A[self.L]

    def backward(self, y):
        m = y.shape[0]
        self.dW = {}
        self.db = {}
        dA = self.A[self.L] - y
        for l in reversed(range(1, self.L+1)):
            if l == self.L:
                dZ = dA
            else:
                if self.activation_type == 'sigmoid':
                    dZ = dA * dsigmoid(self.A[l])
                else:
                    dZ = dA * drelu(self.Z[l])
            self.dW[l] = self.A[l-1].T @ dZ / m
            self.db[l] = np.sum(dZ, axis=0, keepdims=True) / m
            dA = dZ @ self.W[l].T

