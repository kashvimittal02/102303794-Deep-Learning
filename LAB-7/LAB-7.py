    plt.legend()
    plt.show()

    plt.figure()
    plt.plot(train_acc_hist, label="Train Acc")
    plt.plot(val_acc_hist, label="Val Acc")
    plt.title(f"{optimizer_class.__name__} - Accuracy")
    plt.legend()
    plt.show()

